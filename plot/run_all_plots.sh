#!/bin/bash
# ============================================================
# 运行所有 TensorBoard 日志画图程序
# ============================================================

set -e

# 获取脚本所在目录
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
cd "${SCRIPT_DIR}"

echo "=============================================="
echo "  开始绘制所有实验的训练曲线"
echo "=============================================="
echo ""

# 创建输出目录
mkdir -p output

# 1. VLM Adapter 预训练
echo "----------------------------------------------"
echo "  [1/5] 绘制 VLM Adapter 预训练实验曲线..."
echo "----------------------------------------------"
python plot_vlm_pretrain_adapter.py
echo ""

# 2. VLM SFT 冻结 ViT
echo "----------------------------------------------"
echo "  [2/5] 绘制 VLM SFT (冻结 ViT) 实验曲线..."
echo "----------------------------------------------"
python plot_vlm_sft_freeze_vit.py
echo ""

# 3. VLM SFT 全参微调
echo "----------------------------------------------"
echo "  [3/5] 绘制 VLM SFT (全参微调) 实验曲线..."
echo "----------------------------------------------"
python plot_vlm_sft_full.py
echo ""

# 4. MonetVLM SFT
echo "----------------------------------------------"
echo "  [4/5] 绘制 MonetVLM SFT 实验曲线..."
echo "----------------------------------------------"
python plot_monet_sft.py
echo ""

# 5. MonetVLM GRPO (可能还在训练中)
echo "----------------------------------------------"
echo "  [5/5] 绘制 MonetVLM GRPO 实验曲线..."
echo "----------------------------------------------"
python plot_monet_grpo.py
echo ""

echo "=============================================="
echo "  所有实验曲线绘制完成!"
echo "  输出目录: ${SCRIPT_DIR}/output/"
echo "=============================================="

# 列出生成的图片
echo ""
echo "生成的图片文件:"
ls -lh output/*.png 2>/dev/null || echo "  (没有找到生成的图片文件)"
